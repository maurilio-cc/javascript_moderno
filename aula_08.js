//Operadores bitwise

//são operadores que trabalham nos bits dos elementos
//com esses operadores é possível manipular as variáveis bit a bit

/*
Por que usar bitwise aqui?

    Eficiência de memória/banda – Em sistemas embarcados cada byte conta.
    Em vez de mandar três bytes (um para cada leitura), você compacta tudo em um só.

    Velocidade – Operações bit a bit são instruções de máquina extremamente rápidas (shifts, AND, OR).

    Controle de hardware – Registradores de periféricos costumam usar bits individuais para flags.
    Manipular esses flags diretamente é a maneira padrão de configurar/ler dispositivos.
*/

